class Product {
  final String categoryid;
  final String id;
  final double price;
  final String productName;
  final String productWeight;
  final String imagePath;

  Product({
    required this.categoryid,
    required this.id,
    required this.price,
    required this.productName,
    required this.productWeight,
    required this.imagePath,
  });
}
